var=raw_input()
if(var=='A' or var=='a' or var=='E' or var=='e' or var=='i' or var=='I' or var=='o' or var=='O' or var=='u' or var=='U'):
  print("Vowel")
elif(var!='A' or var!='a' or var!='e' or var!='E' or var!='i' or var!='o' or var!='O' or var!='u' or var!='U' or var!='I'):
  print("Consonant")
else:
  print("invalid")
